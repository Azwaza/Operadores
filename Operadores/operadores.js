function somar(){
    var valor1 = document.getElementById('valor1').value;
    var valor2 = document.getElementById('valor2').value;
    var result = parseInt(valor1) + parseInt(valor2);
    alert(result);
}
function sub(){
    var valor1 = document.getElementById('valor1').value;
    var valor2 = document.getElementById('valor2').value;
    var result = parseInt(valor1) - parseInt(valor2);
    alert(result);
}
function dividir(){
    var valor1 = document.getElementById('valor1').value;
    var valor2 = document.getElementById('valor2').value;
    var result = parseInt(valor1) / parseInt(valor2);
    alert(result);
}
function multiplicar(){
    var valor1 = document.getElementById('valor1').value;
    var valor2 = document.getElementById('valor2').value;
    var result = parseInt(valor1) * parseInt(valor2);
    alert(result);
}